	public static int coldest(double[] temperatures) {
		/**
		 * Set the first day as 1, the coldest day as day 1 and store the coldest day as the first temperature
		 */
		double store = temperatures[0];
		int Day = 1 ;
		int ColdestDay = 1;
		
		
		/**
		 * go through the values and when there is a colder day then the previous coldest, set the new coldest day as the current day
		 */
		for (int i = 0; i < temperatures.length; i++) {
			if (temperatures[i] < store) {
			ColdestDay = Day;
			store = temperatures[i];
				}
			/**
			 * Increase the day by 1
			 */
			Day++;
		
	} return ColdestDay;
	}


